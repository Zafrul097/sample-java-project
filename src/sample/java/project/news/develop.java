package sample.java.project.news;

public class develop {

    int roll;
    string address;

    public develop(string address) {
        this.address = address;
    }

    public string getAddress() {
        return address;
    }

    public void setAddress(string address) {
        this.address = address;
    }

    int id;

    public develop(int roll, int id) {
        this.roll = roll;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRoll() {
        return roll;
    }

    public void setRoll(int roll) {
        this.roll = roll;
    }
}
